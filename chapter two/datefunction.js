// date function
var date = new Date();
console.log(date);
console.log(date.toString());//date to string
console.log(date.toTimeString());//Time to string
console.log(date.toDateString());//Date to String
console.log(date.toLocaleDateString());//Locale Date String
console.log(date.getDay());//Day
console.log(date.getDate());//Date
console.log(date.getFullYear());//Full Year
console.log(date.getMonth());//Month
console.log(date.getHours());//Hours
console.log(date.getSeconds());//secounds
console.log(date.getMilliseconds());//Milli Seconds